int	ft_str_is_alpha(char *str)
{
	int	p;

	p = 0;
	while (str[p] != '\0')
	{
		if ((str[p] < 'A') || (str[p] > 'Z' && str[p] < 'a') || (str[p] > 'z'))
		{
			return (0);
		}
		p++;
	}
	return (1);
}
