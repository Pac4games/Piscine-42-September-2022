int	ft_str_is_numeric(char *str)
{
	int	p;

	p = 0;
	while (str[p] != '\0')
	{
		if ((str[p] < '0') || (str[p] > '9'))
		{
			return (0);
		}
		p++;
	}
	return (1);
}
