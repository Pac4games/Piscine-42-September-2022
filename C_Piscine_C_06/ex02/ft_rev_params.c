#include <unistd.h>

int	main(int ac, char **av)
{
	int	p;
	int	i;

	i = ac - 1;
	while (i >= 1)
	{
		p = 0;
		while (av[i][p] != '\0')
		{
			write(1, &av[i][p], 1);
			p++;
		}
		write(1, "\n", 1);
		i--;
	}
	return (0);
}
